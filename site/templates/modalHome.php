                <p>A coleção A Espada Selvagem de Conan está retornando após alguns altos e baixos.</p>
                <p>Em 2017, a Salvat distribuiu os primeiros volumes em bancas na chamada “fase de testes”, para verificar a viabilidade e aceitação do produto. Com a resposta positiva, o lançamento foi marcado para 2018, inclusive com a abertura dos pacotes de assinatura. Entretanto, seguidos atrasos levaram o projeto a ser suspenso sem previsão de retorno.</p>
                <p>Agora, a espera acabou.</p>
                <p>A Panini Comics anunciou, em parceria com a Salvat, a nova coleção A Espada Selvagem de Conan, composta de edições quinzenais e histórias completamente restauradas. Seguindo a ordem de publicação original, a coleção de 75 volumes traz as aventuras apresentadas nos clássicos títulos Savage Tales e Savage Sword of Conan, dos anos 1970, lançados pela Marvel Comics.</p>
                <p>Com os talentos de Barry Windsor-Smith, Gil Kane, John Buscema, Alfredo Alcala, entre outros, a coletânea apresenta uma das séries mais icônicas e populares de fantasia heroica, que esteve fora de catálogo por anos.</p>
                <p>A partir do dia 6 de setembro, edições estarão disponíveis nas bancas. A primeira sairá por R$ 9,90.</p>
                <p>Além desta coleção, a Panini lançou duas revistas bimestrais do cimério: Conan, o Bárbaro e A Espada Selvagem de Conan, que publicam as novas histórias do personagem criado por Robert E. Howard depois que a Marvel readquiriu os direitos de publicação, em 2018.</p>
