                <p>Além desta coleção, a Panini lançou duas revistas bimestrais do cimério: Conan, o Bárbaro e A Espada Selvagem de Conan, que publicam as novas histórias do personagem criado por Robert E. Howard depois que a Marvel readquiriu os direitos de publicação, em 2018.</p>
